"""
A calculator module

"""


def add(x, y):
    return x + y


def sub(x, y):
    return x - y


def mul(x, y):
    return x * y


def div(x, y):
    return x / y


# build a fucntion to calculate the power of a number with x as the base and y as the exponent
def power(x, y):
    return x**y


# build a function to calculate the square root of a number
def sqrt(x):
    return x**0.5
